/* 
Написать программу, которая определяет
какая из цифр трехзначного числа больше: первая или вторая
*/

#include <iostream>
using namespace std;

int main()
{
    setlocale(LC_ALL, "Rus");
    int x;
    cin >> x;
    int b = x % 100 / 10;
    int c = x / 100;
    (c > b) ? cout << c << " - первая цифра" :
        cout << b << " - вторая цифра";
}
