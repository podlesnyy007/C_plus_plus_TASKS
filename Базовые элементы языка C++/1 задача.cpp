/* 
Написать программу, которая вычисляет значения выражения.
Замечания
1.Для вывода результата использовать манипуляторы для форматирования выходного потока.
2.Считать, что вводимые значения x принадлежат области определения выражения.
*/

#include <iostream>
#include <cmath>
#include <iomanip>
using namespace std;

int main()
{
	int x, y;
	cin >> x >> y;
	double p = 3.14;
	cout << setprecision(6) << cos(x) / (p - 2 * x) + 16 * x * cos(x * y);
}
